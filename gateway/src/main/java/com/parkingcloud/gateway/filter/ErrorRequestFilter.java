package com.parkingcloud.gateway.filter;

import com.netflix.zuul.ZuulFilter;
import com.netflix.zuul.context.RequestContext;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.servlet.http.HttpServletRequest;

/**
 * Created by caryc on 2017/3/28.
 */
public class ErrorRequestFilter extends ZuulFilter{
    private static Logger logger = LoggerFactory.getLogger(ErrorRequestFilter.class);

    @Override
    public String filterType() {
        return "error";
    }

    @Override
    public int filterOrder() {
        return 1;
    }

    @Override
    public boolean shouldFilter() {
        return true;
    }

    @Override
    public Object run() {
        RequestContext ctx = RequestContext.getCurrentContext();
        HttpServletRequest request = ctx.getRequest();
        logger.info(String.format("send request %s failed.", request.getMethod(), request.getRequestURL().toString()));
        return null;
    }
}
